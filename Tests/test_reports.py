class TestAccuracyReportValidation:
    """Validate accuracy_report.json structure and content."""

    def test_accuracy_report_has_required_keys(self, sample_accuracy_report_dict):
        data = sample_accuracy_report_dict
        validate_required_keys(data, ["summary", "results"], "accuracy_report root")
        validate_required_keys(data["summary"], ["total", "average_overall_score", "type_counts", "category_averages"], "summary")
        assert isinstance(data["results"], list)
        for result in data["results"]:
            validate_required_keys(result, ["question_id", "question", "query_type", "overall_score"], "result item")

    def test_accuracy_report_score_ranges(self, sample_accuracy_report_dict):
        summary = sample_accuracy_report_dict["summary"]
        validate_numeric_range(summary["average_overall_score"], 0.0, 1.0, "average_overall_score")
        for result in sample_accuracy_report_dict["results"]:
            validate_numeric_range(result["overall_score"], 0.0, 1.0, "overall_score")
            for metric in result.get("metrics", []):
                if "score" in metric:
                    validate_numeric_range(metric["score"], 0.0, 1.0, f"metric {metric.get('metric_name')}")

    def test_accuracy_report_question_count_consistency(self, sample_accuracy_report_dict):
        summary = sample_accuracy_report_dict["summary"]
        results = sample_accuracy_report_dict["results"]
        assert len(results) == summary["total"]
        type_counts = summary["type_counts"]
        count_from_results = {t: 0 for t in ["structured", "semantic", "hybrid"]}
        for r in results:
            count_from_results[r["query_type"]] += 1
        assert type_counts["structured"] == count_from_results["structured"]
        assert type_counts["semantic"] == count_from_results["semantic"]
        assert type_counts["hybrid"] == count_from_results["hybrid"]

    def test_accuracy_report_no_null_values(self, sample_accuracy_report_dict):
        def check_no_none(obj):
            if isinstance(obj, dict):
                for v in obj.values():
                    check_no_none(v)
            elif isinstance(obj, list):
                for item in obj:
                    check_no_none(item)
            else:
                assert obj is not None, "Found None value"
        check_no_none(sample_accuracy_report_dict)

    def test_accuracy_report_metric_passed_correct(self, sample_accuracy_report_dict):
        for result in sample_accuracy_report_dict["results"]:
            for metric in result.get("metrics", []):
                if "score" in metric and "passed" in metric:
                    assert isinstance(metric["passed"], bool)


