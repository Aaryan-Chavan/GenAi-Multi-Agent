def validate_numeric_range(value: Union[int, float], min_val: float, max_val: float, name: str = "value") -> None:
    """Assert that a numeric value is within [min_val, max_val]."""
    assert min_val <= value <= max_val, f"{name} = {value} outside range [{min_val}, {max_val}]"

def validate_non_negative(value: Union[int, float], name: str = "value") -> None:
    """Assert that a numeric value is >= 0."""
    assert value >= 0, f"{name} = {value} is negative"

def validate_required_keys(data: Dict[str, Any], required_keys: List[str], context: str = "") -> None:
    """Assert that all required keys exist in the dictionary."""
    for key in required_keys:
        assert key in data, f"Missing required key '{key}' in {context}"

